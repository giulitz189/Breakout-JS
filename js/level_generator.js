// Classic levels loading procedure
function generateLevel(lvlNo, brickwall, bcc, brc) {
  for (c = 0; c < bcc; c++) {
    brickwall.bricks[c] = [];
    for (r = 0; r < brc; r++) {
      var stat = 0;
      if (levels[lvlNo][c][r] > 0) {
        stat = 1;
        brickwall.remaining++;
      }
      brickwall.bricks[c][r] = { x: 0, y: 0, status: stat, color: levels[lvlNo][c][r] };
    }
  }
}

// Custom level loading procedure
function generateCustomLevel(jsonTable, brickwall, bcc, brc) {
  for (c = 0; c < bcc; c++) {
    brickwall.bricks[c] = [];
    for (r = 0; r < brc; r++) {
      var stat = 0;
      if (jsonTable[c][r] > 0) {
        stat = 1;
        brickwall.remaining++;
      }
      brickwall.bricks[c][r] = { x: 0, y: 0, status: stat, color: jsonTable[c][r] };
    }
  }
}